public class ThirdClass {
    private int yeah;
    private String nah;
    private final boolean ABSOLUTE;

    public ThirdClass(int yeah, String nah, boolean ABSOLUTE) {
        this.yeah = yeah;
        this.nah = nah;
        this.ABSOLUTE = ABSOLUTE;
    }

    public int getYeah() {
        return yeah;
    }

    public void setYeah(int yeah) {
        this.yeah = yeah;
    }

    public String getNah() {
        return nah;
    }

    public void setNah(String nah) {
        this.nah = nah;
    }

    public boolean isABSOLUTE() {
        return ABSOLUTE;
    }
}
