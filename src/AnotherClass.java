public class AnotherClass {
    private int someThing;
    private boolean otherThing;
    private char thing;

    public AnotherClass(int someThing, boolean otherThing, char thing) {
        this.someThing = someThing;
        this.otherThing = otherThing;
        this.thing = thing;
    }

    public int getSomeThing() {
        return someThing;
    }

    public void setSomeThing(int someThing) {
        this.someThing = someThing;
    }

    public boolean isOtherThing() {
        return otherThing;
    }

    public void setOtherThing(boolean otherThing) {
        this.otherThing = otherThing;
    }

    public char getThing() {
        return thing;
    }

    public void setThing(char thing) {
        this.thing = thing;
    }
}
